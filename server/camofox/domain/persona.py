def build_browser_persona(user_id: str) -> dict[str, Any]:
    """Deterministically build a browser persona from a *user_id*.

    Same *user_id* always produces the same persona.
    If *user_id* is in :data:`MANAGED_BROWSER_PERSONAS`, returns a fixed
    persona with ``os='windows'`` and French residential locale profile.
    Otherwise, uses SHA-256 hashing to pseudo-randomly select all parameters.

    The returned dict matches the shape expected by camoufox-js:

    .. code-block:: python

        {
            "version": 2,
            "os": str,
            "locale": str,
            "languages": [str],
            "timezoneId": str,
            "geolocation": {"latitude": float, "longitude": float},
            "screen": {"width": int, "height": int},
            "window": {"outerWidth": int, "outerHeight": int},
            "viewport": {"width": int, "height": int},
            "deviceScaleFactor": float,
            "hardwareConcurrency": int,
            "deviceMemory": int,
            "webgl": None | dict,
            "firefoxUserPrefs": dict,
            "launchScreenConstraints": {"minWidth": int, "maxWidth": int,
                                        "minHeight": int, "maxHeight": int},
        }
    """
    user_id = normalize_user_id(user_id)
    fixed = _build_managed_browser_persona(user_id)

    # --- OS ---
    os_value: str = (
        fixed["os"]
        if fixed
        else PERSONA_OS[_deterministic_index(user_id, "os", len(PERSONA_OS))]
    )

    # --- Locale profile ---
    locale_profile: dict[str, Any] = (
        fixed["localeProfile"]
        if fixed
        else PERSONA_LOCALES[_deterministic_index(user_id, "locale", len(PERSONA_LOCALES))]
    )

    # --- Screen ---
    screen: dict[str, int] = (
        fixed["screen"]
        if fixed
        else PERSONA_SCREENS[_deterministic_index(user_id, "screen", len(PERSONA_SCREENS))]
    )

    # --- Hardware concurrency ---
    hardware_concurrency: int = (
        fixed["hardwareConcurrency"]
        if fixed
        else PERSONA_HARDWARE_CONCURRENCY[
            _deterministic_index(
                user_id, "hardware-concurrency", len(PERSONA_HARDWARE_CONCURRENCY)
            )
        ]
    )

    # --- Device memory ---
    device_memory: int = (
        fixed["deviceMemory"]
        if fixed
        else PERSONA_DEVICE_MEMORY_GB[
            _deterministic_index(user_id, "device-memory", len(PERSONA_DEVICE_MEMORY_GB))
        ]
    )

    # --- Device scale factor ---
    device_scale_factor: float = (
        fixed["deviceScaleFactor"]
        if fixed
        else PERSONA_DEVICE_SCALE_FACTORS[
            _deterministic_index(
                user_id, "device-scale-factor", len(PERSONA_DEVICE_SCALE_FACTORS)
            )
        ]
    )

    # --- Languages ---
    languages: list[str] = locale_profile.get(
        "languages",
        [locale_profile["locale"], locale_profile["locale"].split("-")[0]],
    )

    # --- WebGL (Phase 2: generated by pythonlib camoufox.webgl.sample_webgl) ---
    webgl: Any = fixed.get("webgl") if fixed else None

    # --- Assemble result ---
    result: dict[str, Any] = {
        "version": 2,
        "os": os_value,
        "locale": locale_profile["locale"],
        "languages": languages,
        "timezoneId": locale_profile["timezoneId"],
        "geolocation": locale_profile["geolocation"],
        "screen": {"width": screen["width"], "height": screen["height"]},
        "window": {
            "outerWidth": screen["width"],
            "outerHeight": screen["height"],
        },
        "viewport": {
            "width": screen["width"],
            "height": screen["height"],
        },
        "deviceScaleFactor": device_scale_factor,
        "hardwareConcurrency": hardware_concurrency,
        "deviceMemory": device_memory,
        "webgl": webgl,
        "firefoxUserPrefs": {
            "browser.startup.page": 0,
            "browser.sessionstore.resume_from_crash": True,
            "privacy.resistFingerprinting": False,
        },
        "launchScreenConstraints": {
            "minWidth": screen["width"],
            "maxWidth": screen["width"],
            "minHeight": screen["height"],
            "maxHeight": screen["height"],
        },
    }

    if webgl:
        result["webglConfig"] = [webgl["vendor"], webgl["renderer"]]

    return result
