# ---------------------------------------------------------------------------
# Internal helpers
# ---------------------------------------------------------------------------


def _deterministic_index(user_id: str, salt: str, size: int) -> int:
    """Deterministic index into a pool of *size* items using SHA-256.

    Matches Node.js ``digest.readUInt32BE(0) % size``.
    """
    data = f"{salt}:{user_id}".encode()
    digest = hashlib.sha256(data).digest()
    return int.from_bytes(digest[:4], "big") % size


def _clamp_screen(
    screen: dict[str, int],
    bounds: Optional[dict[str, int]] = None,
) -> dict[str, int]:
    """Clamp screen dimensions to not exceed the visible display bounds."""
    if bounds is None:
        bounds = MANAGED_LEBONCOIN_VISIBLE_DISPLAY
    return {
        "width": min(screen["width"], bounds["width"]),
        "height": min(screen["height"], bounds["height"]),
    }


def _build_managed_browser_persona(user_id: str) -> Optional[dict[str, Any]]:
    """Build a persona for a managed user ID, or return None if not managed."""
    profile = MANAGED_BROWSER_PERSONAS.get(user_id)
    if profile is None:
        return None
    return {
        "os": "windows",
        "localeProfile": FRENCH_RESIDENTIAL_LOCALE_PROFILE,
        "screen": _clamp_screen(profile["screen"]),
        "hardwareConcurrency": profile["hardwareConcurrency"],
        "deviceMemory": profile["deviceMemory"],
        "deviceScaleFactor": 1,
        "webgl": None,
    }


